function Backup-TeamsSettings {
    param([string]$BackupPath)
    
    try {
        $settingsToBackup = @{
            "DesktopConfig" = "$env:APPDATA\Microsoft\Teams\desktop-config.json"
            "UserSettings" = "$env:APPDATA\Microsoft\Teams\settings.json"
            "RegistrySettings" = "HKCU:\SOFTWARE\Microsoft\Office\Teams"
        }
        
        foreach ($setting in $settingsToBackup.GetEnumerator()) {
            if ($setting.Key -eq "RegistrySettings") {
                if (Test-Path $setting.Value) {
                    $exportPath = Join-Path $BackupPath "Teams_Registry_Backup.reg"
                    $regCommand = "reg export `"$($setting.Value)`" `"$exportPath`" /y"
                    Invoke-Expression $regCommand
                    
                    if (Test-Path $exportPath) {
                        Write-EventLogEntry "Teams registry settings backed up to: $exportPath"
                    }
                }
            } else {
                if (Test-Path $setting.Value) {
                    $backupFile = Join-Path $BackupPath "$($setting.Key).json"
                    Copy-Item $setting.Value $backupFile -Force
                    Write-EventLogEntry "Teams settings backed up: $($setting.Key)"
                }
            }
        }
        
        return $true
