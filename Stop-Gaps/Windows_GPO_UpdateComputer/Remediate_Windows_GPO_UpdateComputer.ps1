# Execute Group Policy computer refresh
function Invoke-GroupPolicyComputerRefresh {
    Write-EventLogEntry -Message "Executing Group Policy computer refresh" -EventID 2004
    
    try {
        $startTime = Get-Date
        
        # Build gpupdate command parameters
        $gpupdateArgs = @("/target:computer")
        
        if ($ForceRefresh) {
            $gpupdateArgs += "/force"
            Add-Action "Force refresh enabled - all policies will be reapplied"
        }
        
        if ($SynchronousProcessing) {
            $gpupdateArgs += "/sync"
            Add-Action "Synchronous processing enabled - waiting for policy application"
        }
        
        # Execute gpupdate command
        Add-Action "Executing gpupdate with parameters: $($gpupdateArgs -join ' ')"
        
        try {
            $gpupdateProcess = Start-Process -FilePath "gpupdate" -ArgumentList $gpupdateArgs -Wait -NoNewWindow -PassThru -RedirectStandardOutput "$env:TEMP\gpupdate_output.txt" -RedirectStandardError "$env:TEMP\gpupdate_error.txt"
            
            $exitCode = $gpupdateProcess.ExitCode
            $endTime = Get-Date
            $processingTime = ($endTime - $startTime).TotalSeconds
            
            $script:RemediationResults.ProcessingTime = "$processingTime seconds"
            
            # Read command output
            $output = ""
            $errorOutput = ""
            
            if (Test-Path "$env:TEMP\gpupdate_output.txt") {
                $output = Get-Content "$env:TEMP\gpupdate_output.txt" -Raw -ErrorAction SilentlyContinue
                Remove-Item "$env:TEMP\gpupdate_output.txt" -Force -ErrorAction SilentlyContinue
            }
            
            if (Test-Path "$env:TEMP\gpupdate_error.txt") {
                $errorOutput = Get-Content "$env:TEMP\gpupdate_error.txt" -Raw -ErrorAction SilentlyContinue
                Remove-Item "$env:TEMP\gpupdate_error.txt" -Force -ErrorAction SilentlyContinue
            }
            
            # Analyze results based on exit code
            switch ($exitCode) {
                0 {
                    Add-Action "Group Policy computer refresh completed successfully"
                    Write-EventLogEntry -Message "GPUpdate completed successfully in $processingTime seconds" -EventID 2005
                    return $true
                }
