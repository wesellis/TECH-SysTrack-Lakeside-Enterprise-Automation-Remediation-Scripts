# Add action to tracking
function Add-Action {
    param(
        [Parameter(Mandatory=$true)]
        [string]$Action,
        
        [Parameter(Mandatory=$false)]
        [string]$Result = "Completed"
    )
    
    $actionEntry = @{
        Action = $Action
        Result = $Result
        Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    }
    
    $script:RemediationResults.Actions += $actionEntry
    Write-EventLogEntry -Message "$Action - $Result" -EventID 2001
}

# Add error to tracking
function Add-Error {
    param(
        [Parameter(Mandatory=$true)]
        [string]$Error,
        
        [Parameter(Mandatory=$false)]
        [string]$Context = "General"
    )
    
    $errorEntry = @{
        Error = $Error
        Context = $Context
        Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    }
    
    $script:RemediationResults.Errors += $errorEntry
    Write-EventLogEntry -Message "$Context Error: $Error" -EntryType Error -EventID 3000
}

