# Add issue to tracking
function Add-Issue {
    param(
        [Parameter(Mandatory=$true)]
        [string]$Issue,
        
        [Parameter(Mandatory=$false)]
        [int]$Severity = 1
    )
    
    $script:IssuesFound += $Issue
    $script:DetectionResults.Issues += @{
        Issue = $Issue
        Severity = $Severity
        Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    }
    
    # Reduce health score based on severity
    $script:HealthScore -= ($Severity * 10)
    if ($script:HealthScore -lt 0) { $script:HealthScore = 0 }
    
    Write-EventLogEntry -Message $Issue -EntryType Warning -EventID 2001
}

