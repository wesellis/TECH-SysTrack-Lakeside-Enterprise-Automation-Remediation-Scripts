        [hashtable]$PolicySettings,
        [hashtable]$RefreshStatus,
        [hashtable]$DomainInfo,
        [hashtable]$EventAnalysis
    )
    
    try {
        Write-DetectionLog "Calculating user policy health score..."
        
        $HealthComponents = @{
            UserContextHealth = 0
            PolicySettingsHealth = 0
            RefreshStatusHealth = 0
            DomainConnectivityHealth = 0
            EventLogHealth = 0
        }
        
        # User Context Health (25% weight)
        $UserContextScore = 0
        if ($UserContext.IsAuthenticated) { $UserContextScore += 30 }
        if ($UserContext.ProfileAccessible) { $UserContextScore += 40 }
        if ($UserContext.SessionType -ne "Unknown") { $UserContextScore += 30 }
        $HealthComponents.UserContextHealth = $UserContextScore
        
        # Policy Settings Health (25% weight)
        $PolicySettingsScore = 0
        if ($PolicySettings.RegistryAccessible) { $PolicySettingsScore += 40 }
        if ($PolicySettings.PolicyKeysPresent) { $PolicySettingsScore += 35 }
        if (-not $PolicySettings.PolicyErrorsFound) { $PolicySettingsScore += 25 }
        $HealthComponents.PolicySettingsHealth = $PolicySettingsScore
        
        # Refresh Status Health (25% weight)
        $RefreshStatusScore = 0
        if ($RefreshStatus.GPResultAvailable) { $RefreshStatusScore += 30 }
        if ($RefreshStatus.UserPoliciesApplied) { $RefreshStatusScore += 40 }
        if (-not $RefreshStatus.RefreshNeeded) { $RefreshStatusScore += 30 }
        $HealthComponents.RefreshStatusHealth = $RefreshStatusScore
        
        # Domain Connectivity Health (15% weight)
        $DomainConnectivityScore = 0
        if ($DomainInfo.IsDomainJoined) { $DomainConnectivityScore += 30 }
        if ($DomainInfo.DomainControllerReachable) { $DomainConnectivityScore += 40 }
        if ($DomainInfo.UserDomainValidated) { $DomainConnectivityScore += 30 }
        $HealthComponents.DomainConnectivityHealth = $DomainConnectivityScore
        
        # Event Log Health (10% weight)
        $EventLogScore = 100
        if ($EventAnalysis.RecentErrors.Count -gt 0) { 
            $EventLogScore -= ($EventAnalysis.RecentErrors.Count * 20)
        }
        if ($EventAnalysis.RecentWarnings.Count -gt 3) { 
            $EventLogScore -= ($EventAnalysis.RecentWarnings.Count * 5) 
        }
        $EventLogScore = [Math]::Max(0, $EventLogScore)
        $HealthComponents.EventLogHealth = $EventLogScore
        
        # Calculate weighted overall score
        $OverallScore = [Math]::Round((
            ($HealthComponents.UserContextHealth * 0.25) +
            ($HealthComponents.PolicySettingsHealth * 0.25) +
            ($HealthComponents.RefreshStatusHealth * 0.25) +
            ($HealthComponents.DomainConnectivityHealth * 0.15) +
            ($HealthComponents.EventLogHealth * 0.10)
        ), 2)
        
        Write-DetectionLog "Health score calculated: $OverallScore (User Context: $($HealthComponents.UserContextHealth), Policy Settings: $($HealthComponents.PolicySettingsHealth), Refresh Status: $($HealthComponents.RefreshStatusHealth), Domain: $($HealthComponents.DomainConnectivityHealth), Events: $($HealthComponents.EventLogHealth))"
        
        return @{
            OverallScore = $OverallScore
            Components = $HealthComponents
        }
