function Generate-HealthClinicData {
    <#
    .SYNOPSIS
        Generates health clinic integration data for tracking system.
    #>
    
    param(
        [Parameter(Mandatory = $true)]
        $ValidationResults,
        
        [Parameter(Mandatory = $true)]
        $ComplianceStatus,
        
        [Parameter(Mandatory = $true)]
        $ClientHealth
    )
    
    Write-LogMessage "Generating health clinic integration data" -Level Info
    
    $HealthData = @{
        Timestamp = Get-Date -Format "yyyy-MM-ddTHH:mm:ssZ"
        ComputerName = $env:COMPUTERNAME
        AutomationType = "SCCM_Application_Deployment_Evaluation"
        HealthMetrics = @{
            EvaluationCycleHealth = @{
                LastEvaluationRecent = $ValidationResults.EvaluationRecent
                CycleCompleted = $ValidationResults.CycleCompleted
                SchedulerHealthy = $ValidationResults.SchedulerHealthy
                Score = if ($ValidationResults.CycleCompleted) { 100 } else { 0 }
            }
            ApplicationCompliance = @{
                TotalApplications = $ComplianceStatus.TotalApplications
                CompliantApplications = $ComplianceStatus.CompliantApplications
                CompliancePercentage = $ComplianceStatus.CompliancePercentage
                FailedApplications = $ComplianceStatus.FailedApplications
                Score = $ComplianceStatus.CompliancePercentage
            }
            ClientHealth = @{
                ServiceRunning = $ClientHealth.ServiceStatus.Healthy
                WMIAccessible = $ClientHealth.WMIHealth.Healthy
                ConnectivityGood = $ClientHealth.ConnectivityHealth.Healthy
                OverallHealth = $ClientHealth.OverallHealth
                Score = $ClientHealth.HealthScore
            }
        }
        IssuesDetected = ($FollowUpResult.Issues.Count -gt 0)
        IssueCount = $FollowUpResult.Issues.Count
        RemediationNeeded = $false
        RecommendationCount = $FollowUpResult.Recommendations.Count
        AutomationSuccess = $FollowUpResult.ValidationSuccess
    }
    
    # Determine if additional remediation is needed
    if ($ComplianceStatus.FailedApplications -gt 0 -or 
        $ComplianceStatus.CompliancePercentage -lt 80 -or 
        -not $ValidationResults.CycleCompleted -or 
        $ClientHealth.HealthScore -lt 75) {
        $HealthData.RemediationNeeded = $true
    }
    
    # Calculate overall automation health score
    $EvalScore = $HealthData.HealthMetrics.EvaluationCycleHealth.Score
    $ComplianceScore = $HealthData.HealthMetrics.ApplicationCompliance.Score
    $ClientHealthScore = $HealthData.HealthMetrics.ClientHealth.Score
    
    $HealthData.OverallAutomationScore = [math]::Round(($EvalScore * 0.3 + $ComplianceScore * 0.4 + $ClientHealthScore * 0.3), 1)
    
    return $HealthData
}

