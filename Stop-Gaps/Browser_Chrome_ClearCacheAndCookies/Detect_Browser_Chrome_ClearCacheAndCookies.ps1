function Test-PerformanceImpact {
    param(
        [int]$TotalCacheSizeMB,
        [int]$TotalCookieCount
    )
    
    if ($TotalCacheSizeMB -gt 1000 -or $TotalCookieCount -gt 25000) {
        return "High"
    }
    elseif ($TotalCacheSizeMB -gt 500 -or $TotalCookieCount -gt 15000) {
        return "Medium"
    }
    elseif ($TotalCacheSizeMB -gt 250 -or $TotalCookieCount -gt 10000) {
        return "Low"
    }
    else {
        return "None"
    }
}

# Main detection logic
try {
    Write-LogEntry "Starting Chrome cache and cookies detection with MaxCacheSizeMB: $MaxCacheSizeMB, MaxCookieCount: $MaxCookieCount"
    
    # Check if Chrome is installed
    $DetectionResult.ChromeInstalled = Test-ChromeInstallation
    
    if (-not $DetectionResult.ChromeInstalled) {
        Write-LogEntry "Chrome not installed - no action required"
        Write-Output ($DetectionResult | ConvertTo-Json -Depth 3)
        exit 0
    }
    
    # Get Chrome user profiles
    $Profiles = Get-ChromeUserProfiles
    
    if ($Profiles.Count -eq 0) {
        Write-LogEntry "No Chrome profiles found - no action required"
        Write-Output ($DetectionResult | ConvertTo-Json -Depth 3)
        exit 0
    }
    
    $DetectionResult.ProfilesFound = $Profiles
    
    # Analyze each profile
    foreach ($Profile in $Profiles) {
        Write-LogEntry "Analyzing profile: $($Profile.UserName)\$($Profile.ProfileName)"
        
        # Measure cache
        $CacheMetrics = Measure-CacheSize -CachePath $Profile.CachePath
        $DetectionResult.TotalCacheSizeMB += $CacheMetrics.SizeMB
        
        if ($CacheMetrics.SizeMB -gt $DetectionResult.MaxCacheSizeFound) {
            $DetectionResult.MaxCacheSizeFound = $CacheMetrics.SizeMB
        }
        
        # Measure cookies
        $CookieMetrics = Measure-CookieCount -CookiePath $Profile.CookiePath
        $DetectionResult.TotalCookieCount += $CookieMetrics.Count
        
        if ($CookieMetrics.Count -gt $DetectionResult.MaxCookieCountFound) {
            $DetectionResult.MaxCookieCountFound = $CookieMetrics.Count
        }
        
        # Track corruption
        if ($CacheMetrics.HasCorruption -or $CookieMetrics.HasCorruption) {
            $DetectionResult.CorruptedFiles += "$($Profile.UserName)\$($Profile.ProfileName)"
        }
        
        Write-LogEntry "Profile $($Profile.ProfileName): Cache $($CacheMetrics.SizeMB)MB, Cookies $($CookieMetrics.Count)"
    }
    
    # Determine performance impact
    $DetectionResult.PerformanceImpact = Test-PerformanceImpact -TotalCacheSizeMB $DetectionResult.TotalCacheSizeMB -TotalCookieCount $DetectionResult.TotalCookieCount
    
    # Determine if action is required
    $DetectionResult.ActionRequired = (
        $DetectionResult.TotalCacheSizeMB -gt $MaxCacheSizeMB -or 
        $DetectionResult.TotalCookieCount -gt $MaxCookieCount -or
        $DetectionResult.CorruptedFiles.Count -gt 0
    )
    
    if ($DetectionResult.ActionRequired) {
        $DetectionResult.RecommendedAction = "Clear Chrome cache and cookies"
        
        $Message = "Chrome cache and cookies cleanup required. Total cache: $($DetectionResult.TotalCacheSizeMB)MB, Total cookies: $($DetectionResult.TotalCookieCount), Performance impact: $($DetectionResult.PerformanceImpact)"
        Write-LogEntry $Message -EntryType Warning
        
        Write-Output ($DetectionResult | ConvertTo-Json -Depth 3)
        exit 1
    }
    else {
        $Message = "Chrome cache and cookies within acceptable limits. Total cache: $($DetectionResult.TotalCacheSizeMB)MB, Total cookies: $($DetectionResult.TotalCookieCount)"
        Write-LogEntry $Message
        
        Write-Output ($DetectionResult | ConvertTo-Json -Depth 3)
        exit 0
    }
