function Write-LogEntry {
    param(
        [string]$Message,
        [ValidateSet("Information", "Warning", "Error")]
        [string]$EntryType = "Information",
        [int]$EventID = 3000
    )
    
    try {
        Write-EventLog -LogName $LogName -Source $Source -EntryType $EntryType -EventId $EventID -Message $Message
        
        switch ($EntryType) {
            "Information" { Write-Host "INFO: $Message" -ForegroundColor Green }
            "Warning" { Write-Host "WARN: $Message" -ForegroundColor Yellow }
            "Error" { Write-Host "ERROR: $Message" -ForegroundColor Red }
        }
    }
    catch {
        Write-Host "LOGGING ERROR: $Message" -ForegroundColor Magenta
    }
}

function Test-ChromeInstallation {
    Write-LogEntry "Verifying Chrome installation status"
    
    $ChromePaths = @(
        "${env:ProgramFiles}\Google\Chrome\Application\chrome.exe",
        "${env:ProgramFiles(x86)}\Google\Chrome\Application\chrome.exe",
        "${env:LOCALAPPDATA}\Google\Chrome\Application\chrome.exe"
    )
    
    foreach ($Path in $ChromePaths) {
        if (Test-Path $Path) {
            Write-LogEntry "Chrome installation verified at: $Path"
            return $true
        }
    }
    
    Write-LogEntry "Chrome installation not found" -EntryType Warning
    return $false
}

