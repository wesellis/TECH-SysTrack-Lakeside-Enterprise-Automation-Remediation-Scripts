function Invoke-DiscoveryDataCollectionCycle {
    <#
    .SYNOPSIS
        Triggers the SCCM discovery data collection cycle using WMI operations.
    #>
    
    param(
        [Parameter(Mandatory = $true)]
        [string]$Mode,
        
        [Parameter(Mandatory = $false)]
        [int]$Timeout = 900
    )
    
    Write-LogMessage "Triggering SCCM discovery data collection cycle (Mode: $Mode)" -Level Info
    
    $TriggerResult = @{
        Success = $false
        TriggerTime = Get-Date
        CompletionTime = $null
        Duration = 0
        SchedulesTriggered = @()
        PolicyOperations = @()
        ValidationResults = @{}
        Issues = @()
    }
    
    try {
        # Get SCCM client instance
        $SMSClient = Get-WmiObject -Namespace $SCCMNamespace -Class "SMS_Client" -ErrorAction Stop
        if (-not $SMSClient) {
            throw "Cannot access SCCM Client WMI class"
        }
        
        # Define schedules to trigger based on mode
        $SchedulesToTrigger = @()
        
        switch ($Mode) {
            'Standard' {
                $SchedulesToTrigger += $DiscoveryScheduleID
                Write-LogMessage "Standard mode: Triggering discovery data collection cycle only" -Level Info
            }
            'Force' {
                $SchedulesToTrigger += $DiscoveryScheduleID
                Write-LogMessage "Force mode: Triggering immediate discovery data collection cycle" -Level Info
            }
            'Complete' {
                $SchedulesToTrigger += $PolicyRetrievalScheduleID
                $SchedulesToTrigger += $DiscoveryScheduleID
                Write-LogMessage "Complete mode: Triggering policy retrieval and discovery data collection" -Level Info
            }
        }
        
        # Trigger each schedule
        foreach ($ScheduleID in $SchedulesToTrigger) {
            try {
                $ScheduleName = switch ($ScheduleID) {
                    $DiscoveryScheduleID { "Discovery Data Collection Cycle" }
                    $PolicyRetrievalScheduleID { "Machine Policy Retrieval Cycle" }
                    default { "Unknown Schedule ($ScheduleID)" }
                }
                
                Write-LogMessage "Triggering $ScheduleName" -Level Info
                
                # Execute the WMI method to trigger the schedule
                $Result = $SMSClient.TriggerSchedule($ScheduleID)
                
                if ($Result.ReturnValue -eq 0) {
                    $TriggerResult.SchedulesTriggered += @{
                        ScheduleID = $ScheduleID
                        ScheduleName = $ScheduleName
                        TriggerTime = Get-Date
                        Success = $true
                    }
                    
                    Write-LogMessage "$ScheduleName triggered successfully" -Level Info
                }
                else {
                    $TriggerResult.Issues += "$ScheduleName trigger failed with return value: $($Result.ReturnValue)"
                    Write-LogMessage "$ScheduleName trigger failed" -Level Warning
                }
                
                # Brief pause between triggers
                Start-Sleep -Seconds 2
            }
            catch {
