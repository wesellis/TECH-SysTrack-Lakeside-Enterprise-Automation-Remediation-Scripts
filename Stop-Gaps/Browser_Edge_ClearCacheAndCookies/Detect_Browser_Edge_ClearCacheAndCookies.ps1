        $CookieMetrics = Measure-CookieData -CookiesPath $Profile.CookiesPath -NetworkPath $Profile.NetworkPath
        $TotalCookieCount += $CookieMetrics.Count
        $TotalCookieSize += $CookieMetrics.SizeMB
        $TotalOldCookies += $CookieMetrics.OldCookies
        $TotalTrackingCookies += $CookieMetrics.TrackingCookies
        
        if ($CookieMetrics.Count -gt $MaxProfileCookieCount) {
            $MaxProfileCookieCount = $CookieMetrics.Count
        }
        
        # Track corruption
        if ($CacheMetrics.HasCorruption) {
            $DetectionResult.CorruptedFiles += "$($Profile.UserName)\$($Profile.ProfileName): $($CacheMetrics.CorruptedFileCount) corrupted cache files"
        }
        if ($CookieMetrics.HasCorruption) {
            $DetectionResult.CorruptedFiles += "$($Profile.UserName)\$($Profile.ProfileName): corrupted cookie data"
        }
        
        Write-LogEntry "Profile $($Profile.ProfileName): Cache $($CacheMetrics.SizeMB)MB ($($CacheMetrics.FileCount) files), Cookies ~$($CookieMetrics.Count) ($($CookieMetrics.SizeMB)MB)"
        
