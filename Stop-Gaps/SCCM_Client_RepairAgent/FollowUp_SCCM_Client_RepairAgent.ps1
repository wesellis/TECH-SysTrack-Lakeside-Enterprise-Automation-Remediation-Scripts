function Invoke-OverallValidation {
    Write-LogEntry "Performing overall repair effectiveness assessment"
    
    $HealthyComponents = 0
    $TotalComponents = 4
    
    # Count healthy components
    if ($ValidationResults.ServiceHealth -eq "Healthy") { $HealthyComponents++ }
    if ($ValidationResults.ClientFunctionality -eq "Healthy") { $HealthyComponents++ }
    if ($ValidationResults.WMIConnectivity -eq "Healthy") { $HealthyComponents++ }
    if ($ValidationResults.Communication -eq "Healthy") { $HealthyComponents++ }
    
    # Determine overall health and exit code
    if ($HealthyComponents -eq $TotalComponents) {
        $ValidationResults.OverallHealth = "Healthy"
        $ValidationResults.RepairEffective = $true
        $ExitCode = 0
    }
    elseif ($HealthyComponents -ge 3) {
        $ValidationResults.OverallHealth = "Warning"
        $ValidationResults.RepairEffective = $true
        $ExitCode = 2
    }
    else {
        $ValidationResults.OverallHealth = "Unhealthy"
        $ValidationResults.RepairEffective = $false
        $ExitCode = 1
    }
    
    # Generate recommendations
    if ($ValidationResults.Issues.Count -gt 0) {
        $ValidationResults.Recommendations += "Monitor SCCM client agent for recurring issues"
        $ValidationResults.Recommendations += "Consider escalation if issues persist after repair"
    }
    
    if ($ValidationResults.OverallHealth -eq "Healthy") {
        $ValidationResults.Recommendations += "SCCM client agent repair was successful"
        $ValidationResults.Recommendations += "Continue normal monitoring schedule"
    }
}

