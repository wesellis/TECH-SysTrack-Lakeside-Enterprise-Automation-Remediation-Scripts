function Invoke-OverallRepairAssessment {
    param()
    
    Write-ProgressUpdate "Overall Assessment" "Evaluating repair results" 98
    
    # Count successful repairs and issues
    $SuccessfulRepairs = 0
    $TotalRepairs = 0
    $CriticalFailures = 0
    $WarningIssues = 0
    
    foreach ($RepairCategory in $RepairResults.Keys) {
        if ($RepairCategory -eq "OverallResult") { continue }
        
        $Result = $RepairResults[$RepairCategory]
        if ($Result.Attempted) {
            $TotalRepairs++
            
            if ($Result.Successful) {
                $SuccessfulRepairs++
                if ($Result.Issues.Count -gt 0) {
                    $WarningIssues += $Result.Issues.Count
                }
            } else {
                $CriticalFailures++
            }
        }
    }
    
    # Determine overall result
    $OverallSuccess = $false
    $PartialSuccess = $false
    
    if ($SuccessfulRepairs -eq $TotalRepairs -and $CriticalFailures -eq 0) {
        $OverallSuccess = $true
        $ExitCode = 0
    }
    elseif ($SuccessfulRepairs -gt 0 -and $CriticalFailures -le 1) {
        $PartialSuccess = $true
        $ExitCode = 2
    }
    else {
        $ExitCode = 1
    }
    
    # Update overall results
    $RepairResults.OverallResult.Success = $OverallSuccess
    $RepairResults.OverallResult.PartialSuccess = $PartialSuccess
    $RepairResults.OverallResult.CriticalFailures = $CriticalFailures
    $RepairResults.OverallResult.WarningIssues = $WarningIssues
    $RepairResults.OverallResult.RepairTime = "{0:F1} minutes" -f ((Get-Date) - $RepairStartTime).TotalMinutes
    
    # Log final results
    if ($OverallSuccess) {
        Write-LogEntry "SCCM client agent repair completed successfully - all components repaired" -Level "Information"
    }
    elseif ($PartialSuccess) {
        Write-LogEntry "SCCM client agent repair partially successful - monitoring required" -Level "Warning"
    }
    else {
        Write-LogEntry "SCCM client agent repair failed - critical issues remain" -Level "Error"
    }
    
    Write-ProgressUpdate "Overall Assessment" "Repair assessment completed" 100
}

