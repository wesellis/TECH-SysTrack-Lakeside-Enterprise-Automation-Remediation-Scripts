function Repair-SCCMServices {
    param()
    
    Write-ProgressUpdate "SCCM Service Repair" "Starting service health restoration" 10
    
    $RepairResults.ServiceRepair.Attempted = $true
    
    try {
        # Define SCCM services with repair priority
        $SCCMServices = @(
            @{ Name = "CcmExec"; DisplayName = "SMS Agent Host"; Critical = $true; RestartDependents = $true },
            @{ Name = "ccmsetup"; DisplayName = "Configuration Manager Client Setup"; Critical = $false; RestartDependents = $false }
        )
        
        $ServiceRepairIssues = @()
        $ServicesRepaired = 0
        
        foreach ($ServiceInfo in $SCCMServices) {
            $ServiceName = $ServiceInfo.Name
            $IsCritical = $ServiceInfo.Critical
            
            Write-ProgressUpdate "SCCM Service Repair" "Processing service: $ServiceName" 20
            
            try {
                $Service = Get-Service -Name $ServiceName -ErrorAction SilentlyContinue
                
                if (-not $Service) {
                    if ($IsCritical) {
                        $ServiceRepairIssues += "Critical service not found: $ServiceName"
                        continue
                    } else {
                        Write-LogEntry "Optional service not found: $ServiceName" -Level "Information"
                        continue
                    }
                }
                
                # Stop service if running and problematic
                if ($Service.Status -eq "Running") {
                    Write-LogEntry "Stopping service for repair: $ServiceName"
                    
                    if ($ServiceInfo.RestartDependents) {
                        # Stop dependent services first
                        $DependentServices = Get-Service -Name $ServiceName | Select-Object -ExpandProperty DependentServices
                        foreach ($DepService in $DependentServices) {
                            if ($DepService.Status -eq "Running") {
                                Write-LogEntry "Stopping dependent service: $($DepService.Name)"
                                Stop-Service -Name $DepService.Name -Force -ErrorAction SilentlyContinue
                                Start-Sleep -Seconds 2
                            }
                        }
                    }
                    
                    Stop-Service -Name $ServiceName -Force -ErrorAction Stop
                    Start-Sleep -Seconds 5
                }
                
                # Ensure service is set to automatic startup if critical
                if ($IsCritical) {
                    $ServiceWMI = Get-CimInstance -ClassName Win32_Service -Filter "Name='$ServiceName'" -ErrorAction SilentlyContinue
                    if ($ServiceWMI -and $ServiceWMI.StartMode -ne "Auto") {
                        Write-LogEntry "Setting service to automatic startup: $ServiceName"
                        Set-Service -Name $ServiceName -StartupType Automatic -ErrorAction Stop
                    }
                }
                
                # Start the service
                Write-LogEntry "Starting service: $ServiceName"
                Start-Service -Name $ServiceName -ErrorAction Stop
                
                # Wait for service to stabilize
                $TimeoutSeconds = 30
                $StartTime = Get-Date
                do {
                    Start-Sleep -Seconds 2
                    $Service = Get-Service -Name $ServiceName -ErrorAction SilentlyContinue
                    $ElapsedTime = (Get-Date) - $StartTime
                } while ($Service.Status -ne "Running" -and $ElapsedTime.TotalSeconds -lt $TimeoutSeconds)
                
                if ($Service.Status -eq "Running") {
                    Write-LogEntry "Service repair successful: $ServiceName"
                    $ServicesRepaired++
                } else {
                    $ServiceRepairIssues += "Service failed to start: $ServiceName"
                }
                
                # Restart dependent services if needed
                if ($ServiceInfo.RestartDependents -and $Service.Status -eq "Running") {
                    $DependentServices = Get-Service -Name $ServiceName | Select-Object -ExpandProperty DependentServices
