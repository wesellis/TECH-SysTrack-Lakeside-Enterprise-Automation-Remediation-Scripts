                        $validationStatus.SecurityLevelValid = $true
                        $validationStatus.HealthScore += 25
                        Write-EventLogEntry "VBA Security Level correctly set to 1 for Office $version"
                    } else {
                        $validationStatus.ConfigurationErrors += "VBA Security Level not set to 1 for Office $version (Current: $($settings.VBASecurityLevel))"
                    }
                    
                    # Check VBA Warnings
