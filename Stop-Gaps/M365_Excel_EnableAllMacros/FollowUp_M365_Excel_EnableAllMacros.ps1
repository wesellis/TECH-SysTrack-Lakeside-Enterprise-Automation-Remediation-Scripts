            HealthScore = 0
            ConfigurationErrors = @()
        }
        
        # Check Excel macro settings for all Office versions
        $officeVersions = @("16.0", "15.0")
        
        foreach ($version in $officeVersions) {
            $regPath = "HKCU:\SOFTWARE\Microsoft\Office\$version\Excel\Security"
            
            if (Test-Path $regPath) {
                $validationStatus.OfficeVersionsChecked += $version
                
                try {
                    $settings = Get-ItemProperty $regPath -ErrorAction SilentlyContinue
                    
