    $issues = @()
    $severity = "Low"
    $macroSettings = @{}
    
    try {
        # Check if Office/Excel is installed
        $officeInstallPaths = @(
            "${env:ProgramFiles}\Microsoft Office\root\Office16\EXCEL.EXE",
            "${env:ProgramFiles(x86)}\Microsoft Office\root\Office16\EXCEL.EXE",
            "${env:ProgramFiles}\Microsoft Office\Office16\EXCEL.EXE",
            "${env:ProgramFiles(x86)}\Microsoft Office\Office16\EXCEL.EXE",
            "${env:ProgramFiles}\Microsoft Office\Office15\EXCEL.EXE",
            "${env:ProgramFiles(x86)}\Microsoft Office\Office15\EXCEL.EXE"
        )
        
        $excelInstalled = $false
        $excelPath = $null
        $officeVersion = "Unknown"
        
        foreach ($path in $officeInstallPaths) {
            if (Test-Path $path) {
                $excelInstalled = $true
                $excelPath = $path
                if ($path -match "Office16") {
                    $officeVersion = "Office 2016/2019/2021/365"
                } elseif ($path -match "Office15") {
                    $officeVersion = "Office 2013"
                }
                break
            }
        }
        
        if (-not $excelInstalled) {
            return @{
                IssuesFound = $false
                IssueCount = 0
                Issues = @("Excel not installed")
                Severity = "Info"
                RequiresMacroEnablement = $false
                MacroSettings = @{}
                OfficeVersion = "Not installed"
            }
        }
        
        $macroSettings["ExcelPath"] = $excelPath
        $macroSettings["OfficeVersion"] = $officeVersion
        
        # Check Excel macro security settings in registry
        $excelRegistryPaths = @(
            "HKCU:\SOFTWARE\Microsoft\Office\16.0\Excel\Security",
            "HKCU:\SOFTWARE\Microsoft\Office\15.0\Excel\Security",
            "HKLM:\SOFTWARE\Microsoft\Office\16.0\Excel\Security",
            "HKLM:\SOFTWARE\Microsoft\Office\15.0\Excel\Security",
            "HKLM:\SOFTWARE\Policies\Microsoft\Office\16.0\Excel\Security",
            "HKLM:\SOFTWARE\Policies\Microsoft\Office\15.0\Excel\Security"
        )
        
        $macroSecurityLevel = $null
        $vbaWarningsPolicy = $null
        $accessVBOM = $null
        $blockMacros = $null
        
        foreach ($regPath in $excelRegistryPaths) {
            if (Test-Path $regPath) {
                try {
                    # VBASecurityLevel: 1=Enable all, 2=Prompt, 3=Digitally signed only, 4=Disable all
                    $secLevel = Get-ItemProperty $regPath -Name "VBASecurityLevel" -ErrorAction SilentlyContinue
                    if ($secLevel -and $null -eq $macroSecurityLevel) {
                        $macroSecurityLevel = $secLevel.VBASecurityLevel
                        $macroSettings["VBASecurityLevel"] = $macroSecurityLevel
                        $macroSettings["VBASecuritySource"] = $regPath
                    }
                    
                    # VBAWarnings: 1=Enable all, 2=Disable with notification, 3=Disable all, 4=Disable all except digitally signed
                    $warnings = Get-ItemProperty $regPath -Name "VBAWarnings" -ErrorAction SilentlyContinue
                    if ($warnings -and $null -eq $vbaWarningsPolicy) {
                        $vbaWarningsPolicy = $warnings.VBAWarnings
                        $macroSettings["VBAWarnings"] = $vbaWarningsPolicy
                    }
                    
                    # AccessVBOM: 1=Allow access to VBA object model, 0=Deny access
                    $vbom = Get-ItemProperty $regPath -Name "AccessVBOM" -ErrorAction SilentlyContinue
                    if ($vbom -and $null -eq $accessVBOM) {
                        $accessVBOM = $vbom.AccessVBOM
                        $macroSettings["AccessVBOM"] = $accessVBOM
                    }
                    
                    # Check for macro blocking policies
                    $blockMacroValue = Get-ItemProperty $regPath -Name "BlockMacros" -ErrorAction SilentlyContinue
                    if ($blockMacroValue -and $null -eq $blockMacros) {
                        $blockMacros = $blockMacroValue.BlockMacros
                        $macroSettings["BlockMacros"] = $blockMacros
                    }
                } catch {
                    # Continue checking other paths
                }
            }
        }
        
        # Analyze macro security settings
        if ($null -ne $macroSecurityLevel) {
            switch ($macroSecurityLevel) {
                1 { 
                    $macroSettings["SecurityDescription"] = "Enable all macros (not recommended)"
                    # This is what we want, but check if there are conflicting policies
                }
                2 { 
                    $macroSettings["SecurityDescription"] = "Disable all macros with notification"
                    $issues += "Excel macro security set to prompt for macros (Level 2)"
                    $severity = "Medium"
                }
                3 { 
                    $macroSettings["SecurityDescription"] = "Disable all macros except digitally signed macros"
                    $issues += "Excel macro security set to digitally signed only (Level 3)"
                    $severity = "High"
                }
                4 { 
                    $macroSettings["SecurityDescription"] = "Disable all macros without notification"
                    $issues += "Excel macro security set to disable all macros (Level 4)"
                    $severity = "High"
                }
                default {
                    $issues += "Unknown Excel macro security level: $macroSecurityLevel"
                    $severity = "Medium"
                }
            }
        } else {
            $issues += "Excel macro security level not found in registry"
            $severity = "Medium"
        }
        
        # Check VBA Warnings policy
        if ($null -ne $vbaWarningsPolicy) {
            switch ($vbaWarningsPolicy) {
                1 { 
                    $macroSettings["WarningsDescription"] = "Enable all macros (VBA Warnings = 1)"
                    # This is what we want
                }
                2 { 
                    $macroSettings["WarningsDescription"] = "Disable with notification (VBA Warnings = 2)"
                    $issues += "VBA warnings policy set to disable with notification"
                    if ($severity -eq "Low") { $severity = "Medium" }
