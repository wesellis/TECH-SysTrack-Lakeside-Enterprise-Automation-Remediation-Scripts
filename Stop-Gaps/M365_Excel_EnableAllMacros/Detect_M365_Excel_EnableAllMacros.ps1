                    $issues += "VBA warnings policy set to digitally signed only"
                    $severity = "High"
                }
                default {
                    $issues += "Unknown VBA warnings policy: $vbaWarningsPolicy"
                    if ($severity -eq "Low") { $severity = "Medium" }
                }
            }
        }
        
