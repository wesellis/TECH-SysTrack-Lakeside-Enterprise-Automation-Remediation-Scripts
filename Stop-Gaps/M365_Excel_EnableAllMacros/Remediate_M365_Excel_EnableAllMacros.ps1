            $remediationSteps += "GPO restrictions detected - BLOCKING"
            Write-EventLogEntry "Group Policy restrictions detected: $($gpoRestrictions -join '; ')" -Type "Warning"
            
            if (-not $Force) {
                throw "Group Policy restrictions prevent macro enablement. Use -Force to override."
            } else {
                Write-EventLogEntry "Proceeding with -Force flag despite GPO restrictions" -Type "Warning"
                $remediationSteps += "GPO restrictions bypassed with -Force flag"
            }
        } else {
            $remediationSteps += "GPO restrictions check - CLEAR"
        }
        
        # Step 2: Backup current settings
        if ($BackupSettings) {
            Write-Output "Step 2: Backing up Excel security settings..."
            if (Backup-ExcelSettings -BackupPath $backupPath) {
                $remediationSteps += "Settings backup - COMPLETED"
            } else {
                $remediationSteps += "Settings backup - FAILED"
            }
        } else {
            $remediationSteps += "Settings backup - SKIPPED"
        }
        
        # Step 3: Close Excel processes
        Write-Output "Step 3: Closing Excel processes..."
        if (Close-ExcelProcesses) {
            $remediationSteps += "Excel process closure - COMPLETED"
        } else {
            $remediationSteps += "Excel process closure - WARNING (some processes remain)"
