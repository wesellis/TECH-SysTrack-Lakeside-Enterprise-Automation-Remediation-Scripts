# Logging function
function Write-FollowUpLog {
    param([string]$Message, [ValidateSet('Information', 'Warning', 'Error')]$Level = 'Information')
    $Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    $LogMessage = "[$Timestamp] GPO_FullRefresh_FollowUp: $Message"
    
    switch ($Level) {
        'Information' { Write-Host $LogMessage -ForegroundColor Green }
        'Warning' { Write-Warning $LogMessage }
        'Error' { Write-Error $LogMessage }
    }
    
    if ($EnableDetailedLogging) {
        $EventId = switch ($Level) { 'Information' { 3001 } 'Warning' { 3002 } 'Error' { 3003 } }
        Write-EventLog -LogName 'Application' -Source 'SysTrack-Automation' -EventId $EventId -EntryType $Level -Message $LogMessage -ErrorAction SilentlyContinue
    }
}

