            $RemediationResults.Recommendations += "User policy refresh failed - consider user logoff/logon"
        }
    }
    
    # Failure recommendations
    if (-not $RemediationResults.Success -and -not $RemediationResults.PartialSuccess) {
        $RemediationResults.Recommendations += "Group Policy refresh failed - escalate to domain administrator"
        $RemediationResults.Recommendations += "Check domain connectivity and Group Policy service health"
        $RemediationResults.Recommendations += "Consider manual policy application or system restart"
