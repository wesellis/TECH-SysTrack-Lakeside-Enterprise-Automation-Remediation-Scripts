        # Verify administrative privileges
        $CurrentPrincipal = New-Object Security.Principal.WindowsPrincipal([Security.Principal.WindowsIdentity]::GetCurrent())
        if (-not $CurrentPrincipal.IsInRole([Security.Principal.WindowsBuiltInRole]::Administrator)) {
            throw "Administrative privileges required for Group Policy remediation"
        }
        $RemediationResults.ActionsTaken += "Administrative privilege verification successful"

        # Check domain membership
        $ComputerSystem = Get-WmiObject -Class Win32_ComputerSystem -ErrorAction Stop
        if ($ComputerSystem.PartOfDomain -eq $false) {
            throw "Computer must be domain-joined for Group Policy operations"
        }
        $RemediationResults.PreRemediationState.DomainConnectivityStatus = "Domain member: $($ComputerSystem.Domain)"
        $RemediationResults.ActionsTaken += "Domain membership verification successful"

        # Validate Group Policy service
        $GPService = Get-Service -Name "gpsvc" -ErrorAction Stop
        $RemediationResults.PreRemediationState.GroupPolicyServiceStatus = $GPService.Status.ToString()
        Write-RemediationLog "Group Policy Client service status: $($GPService.Status)" -Level Verbose
        
        if ($GPService.Status -ne 'Running') {
            Write-RemediationLog "Group Policy Client service not running - attempting to start" -Level Warning
            Start-Service -Name "gpsvc" -ErrorAction Stop
            $RemediationResults.ActionsTaken += "Started Group Policy Client service"
        }

        # Test secure channel
        $DomainName = $ComputerSystem.Domain
        $NltestResult = & nltest /sc_query:$DomainName 2>&1
