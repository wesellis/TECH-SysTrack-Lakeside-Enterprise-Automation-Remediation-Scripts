# Create policy configuration backup
function New-PolicyConfigurationBackup {
    Write-RemediationLog "Creating Group Policy configuration backup" -Level Verbose
    
    try {
        if (-not $BackupPolicyConfiguration) {
            Write-RemediationLog "Policy configuration backup disabled - skipping" -Level Verbose
            return $true
        }

        $BackupFolder = "$env:TEMP\GPO_Backup_$(Get-Date -Format 'yyyyMMdd_HHmmss')"
        New-Item -Path $BackupFolder -ItemType Directory -Force | Out-Null
        $RemediationResults.PreRemediationState.BackupLocation = $BackupFolder
        
        # Export current computer policy report
        $ComputerReportPath = Join-Path $BackupFolder "Computer_Policy_Report.html"
        $GPResultComputer = & gpresult /h $ComputerReportPath /scope:computer 2>&1
        if ($LASTEXITCODE -eq 0) {
            Write-RemediationLog "Computer policy report exported to: $ComputerReportPath" -Level Verbose
            $RemediationResults.ActionsTaken += "Computer policy configuration backup created"
        }

        # Export current user policy report
        $UserReportPath = Join-Path $BackupFolder "User_Policy_Report.html"
