# Comprehensive logging function
function Write-RemediationLog {
    param(
        [string]$Message,
        [ValidateSet('Information', 'Warning', 'Error', 'Verbose')]
        [string]$Level = 'Information'
    )
    
    $Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    $LogMessage = "[$Timestamp] GPO_FullRefresh_Remediation: $Message"
    
    switch ($Level) {
        'Information' { 
            Write-Host $LogMessage -ForegroundColor Green
            if ($EnableDetailedLogging) {
                Write-EventLog -LogName 'Application' -Source 'SysTrack-Automation' -EventId 2001 -EntryType Information -Message $LogMessage -ErrorAction SilentlyContinue
            }
        }
        'Warning' { 
            Write-Warning $LogMessage
            if ($EnableDetailedLogging) {
                Write-EventLog -LogName 'Application' -Source 'SysTrack-Automation' -EventId 2002 -EntryType Warning -Message $LogMessage -ErrorAction SilentlyContinue
            }
        }
        'Error' { 
            Write-Error $LogMessage
            if ($EnableDetailedLogging) {
                Write-EventLog -LogName 'Application' -Source 'SysTrack-Automation' -EventId 2003 -EntryType Error -Message $LogMessage -ErrorAction SilentlyContinue
            }
        }
        'Verbose' {
            Write-Verbose $LogMessage
        }
    }
}

