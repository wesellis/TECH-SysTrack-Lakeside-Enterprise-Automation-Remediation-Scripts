        $stuckJobs = @()
        
        if ($printJobs) {
            foreach ($job in $printJobs) {
                # Check for error jobs
                if ($job.Status -like "*Error*" -or $job.Status -like "*Failed*") {
                    $errorJobs += $job
                }
                
                # Check for stuck jobs
                if ($job.TimeSubmitted) {
                    $jobTime = [Management.ManagementDateTimeConverter]::ToDateTime($job.TimeSubmitted)
