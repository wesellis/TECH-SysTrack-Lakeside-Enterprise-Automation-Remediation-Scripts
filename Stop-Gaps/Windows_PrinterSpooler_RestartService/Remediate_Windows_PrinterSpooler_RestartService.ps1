        Write-RemediationLog "Creating backup of Print Spooler configuration"
        
        # Create backup directory
        if (-not (Test-Path $script:BackupLocation)) {
            New-Item -Path $script:BackupLocation -ItemType Directory -Force | Out-Null
        }
        
        # Export printer configurations
        $printers = Get-WmiObject -Class Win32_Printer
        $printerConfig = @()
        
        foreach ($printer in $printers) {
            $printerConfig += @{
                Name = $printer.Name
                DriverName = $printer.DriverName
                PortName = $printer.PortName
                PrinterStatus = $printer.PrinterStatus
                Shared = $printer.Shared
                ShareName = $printer.ShareName
                Location = $printer.Location
                Comment = $printer.Comment
            }
        }
        
        $printerConfig | ConvertTo-Json -Depth 10 | Out-File -FilePath "$script:BackupLocation\PrinterConfiguration.json" -Encoding UTF8
        
        # Export print job information
        $printJobs = Get-WmiObject -Class Win32_PrintJob -ErrorAction SilentlyContinue
        if ($printJobs) {
            $jobConfig = @()
            foreach ($job in $printJobs) {
                $jobConfig += @{
                    JobId = $job.JobId
                    Name = $job.Name
                    Document = $job.Document
                    Owner = $job.Owner
                    Status = $job.Status
                    TimeSubmitted = $job.TimeSubmitted
                    Priority = $job.Priority
                }
            }
            $jobConfig | ConvertTo-Json -Depth 10 | Out-File -FilePath "$script:BackupLocation\PrintJobs.json" -Encoding UTF8
        }
        
        Write-RemediationLog "Configuration backup created at: $script:BackupLocation"
        $script:RemediationActions += "Created configuration backup at $script:BackupLocation"
        
        return $true
