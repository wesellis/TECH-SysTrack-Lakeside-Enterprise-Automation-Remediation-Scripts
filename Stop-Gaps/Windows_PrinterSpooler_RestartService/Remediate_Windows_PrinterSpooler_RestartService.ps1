function Clean-SpoolFolder {
    try {
        Write-RemediationLog "Cleaning spool folder: $script:SpoolFolder"
        
        if (-not (Test-Path $script:SpoolFolder)) {
            Write-RemediationLog "Spool folder does not exist: $script:SpoolFolder" "Warning"
            return $true
        }
        
        # Get spool files before cleaning
        $spoolFiles = Get-ChildItem -Path $script:SpoolFolder -ErrorAction SilentlyContinue
        $totalFiles = $spoolFiles.Count
        $totalSizeMB = [math]::Round((Get-ChildItem -Path $script:SpoolFolder -Recurse | Measure-Object -Property Length -Sum).Sum / 1MB, 2)
        
        Write-RemediationLog "Spool folder contains $totalFiles files ($totalSizeMB MB)"
        
        if ($totalFiles -eq 0) {
            Write-RemediationLog "Spool folder is already clean"
            return $true
        }
        
        # Create backup of spool folder contents list
        $spoolFiles | Select-Object Name, Length, LastWriteTime | ConvertTo-Json | Out-File -FilePath "$script:BackupLocation\SpoolFilesBackup.json" -Encoding UTF8
        
        $cleanedFiles = 0
        $failedFiles = 0
        $cleanedSizeMB = 0
        
        foreach ($file in $spoolFiles) {
            try {
