        Write-RemediationLog "Waiting for service '$ServiceName' to reach state: $DesiredState"
        
        $timeoutTime = (Get-Date).AddSeconds($TimeoutSeconds)
        $serviceReady = $false
        
        while ((Get-Date) -lt $timeoutTime -and -not $serviceReady) {
            $service = Get-Service -Name $ServiceName -ErrorAction SilentlyContinue
            
            if ($service) {
                if ($service.Status -eq $DesiredState) {
                    $serviceReady = $true
                    Write-RemediationLog "Service '$ServiceName' successfully reached state: $DesiredState"
                }
                else {
                    Write-RemediationLog "Service '$ServiceName' current state: $($service.Status), waiting for: $DesiredState"
                    Start-Sleep -Seconds 2
                }
            }
            else {
                Write-RemediationLog "Service '$ServiceName' not found" "Error"
                return $false
            }
        }
        
        if (-not $serviceReady) {
            Write-RemediationLog "Timeout waiting for service '$ServiceName' to reach state: $DesiredState" "Error"
            return $false
        }
        
        return $true
