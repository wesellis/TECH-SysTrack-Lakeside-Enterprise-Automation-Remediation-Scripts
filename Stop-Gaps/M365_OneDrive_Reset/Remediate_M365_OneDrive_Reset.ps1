function Start-OneDriveRemediation {
    $startTime = Get-Date
    $remediationSteps = @()
    $backupPath = "$env:TEMP\OneDrive_Remediation_$(Get-Date -Format 'yyyyMMdd_HHmmss')"
    
    try {
        Write-EventLogEntry "Starting OneDrive remediation process..." -EventId 1001
        
        # Create backup directory
        if (-not (Test-Path $backupPath)) {
            New-Item -Path $backupPath -ItemType Directory -Force | Out-Null
        }
        
        # Step 1: Network connectivity check
        Write-Output "Step 1: Testing network connectivity..."
        if (-not (Test-NetworkConnectivity)) {
            $remediationSteps += "Network connectivity check - FAILED"
            Write-EventLogEntry "Network connectivity issues detected. OneDrive reset may not resolve sync issues." -Type "Warning"
        } else {
            $remediationSteps += "Network connectivity check - PASSED"
        }
        
        # Step 2: Backup current settings
        Write-Output "Step 2: Backing up OneDrive settings..."
        $backupResult = Backup-OneDriveSettings -BackupPath $backupPath
        if ($backupResult) {
            $remediationSteps += "Settings backup - COMPLETED ($backupResult)"
        } else {
            $remediationSteps += "Settings backup - FAILED"
        }
        
        # Step 3: Stop OneDrive processes
        Write-Output "Step 3: Stopping OneDrive processes..."
        if (Stop-OneDriveProcesses) {
            $remediationSteps += "Process termination - COMPLETED"
        } else {
            $remediationSteps += "Process termination - FAILED"
            throw "Could not stop OneDrive processes"
        }
        
        # Step 4: Clear cache and temporary files
        Write-Output "Step 4: Clearing OneDrive cache..."
        if (Clear-OneDriveCache) {
            $remediationSteps += "Cache cleanup - COMPLETED"
        } else {
            $remediationSteps += "Cache cleanup - FAILED"
        }
        
        # Step 5: Reset registry settings
        Write-Output "Step 5: Resetting OneDrive registry settings..."
        if (Reset-OneDriveRegistry) {
            $remediationSteps += "Registry reset - COMPLETED"
        } else {
            $remediationSteps += "Registry reset - FAILED"
        }
        
        # Step 6: Restart OneDrive
        Write-Output "Step 6: Restarting OneDrive service..."
        if (Start-OneDriveService) {
            $remediationSteps += "Service restart - COMPLETED"
        } else {
            $remediationSteps += "Service restart - FAILED"
            throw "Could not restart OneDrive service"
        }
        
        $endTime = Get-Date
        $duration = $endTime - $startTime
        
        # Success output
        Write-EventLogEntry "OneDrive remediation completed successfully in $($duration.TotalSeconds) seconds" -EventId 1002
        
        $result = @{
            Success = $true
            Duration = $duration
            Steps = $remediationSteps
