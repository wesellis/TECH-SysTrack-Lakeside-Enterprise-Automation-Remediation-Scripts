#region Main Remediation Logic

try {
    Write-LogMessage "Starting SCCM Client Cache Clear remediation" -Level Info
    Write-LogMessage "Script version: $ScriptVersion" -Level Info
    Write-LogMessage "Clear mode: $ClearMode" -Level Info
    Write-LogMessage "Maximum item age: $MaxItemAge days" -Level Info
    Write-LogMessage "Preserve persistent items: $PreservePersistent" -Level Info
    Write-LogMessage "Maximum wait time: $MaxWaitTimeMinutes minutes" -Level Info
    
    $OverallStartTime = Get-Date
    
    # Verify SCCM client readiness
    Write-ProgressUpdate -Activity "SCCM Cache Clear" -Status "Verifying SCCM client readiness"
    $ClientReadiness = Test-SCCMClientReady
    $RemediationResult.Details.ClientReadiness = $ClientReadiness
    
    if (-not $ClientReadiness.IsReady) {
        Write-LogMessage "SCCM client is not ready for cache operations" -Level Error
        $RemediationResult.Errors += $ClientReadiness.Issues
        $RemediationResult.ExitCode = 1
        throw "SCCM client not ready for cache operations"
    }
    
    Write-LogMessage "SCCM client is ready for cache operations" -Level Success
    
    # Identify cache items for clearing
    Write-ProgressUpdate -Activity "SCCM Cache Clear" -Status "Analyzing cache items for clearing"
    $ClearingPlan = Get-CacheItemsForClearing -Mode $ClearMode -MaxAge $MaxItemAge -PreservePersistent $PreservePersistent
    $RemediationResult.Details.ClearingPlan = $ClearingPlan
    
    if ($ClearingPlan.Issues.Count -gt 0) {
        $RemediationResult.Warnings += $ClearingPlan.Issues
        Write-LogMessage "Issues detected during cache analysis" -Level Warning
    }
    
    Write-LogMessage "Cache analysis completed - Items to remove: $($ClearingPlan.ItemsToRemove.Count), Estimated savings: $($ClearingPlan.EstimatedSavingsMB) MB" -Level Info
    
    # Perform cache clearing
    if ($ClearingPlan.ItemsToRemove.Count -gt 0) {
        Write-ProgressUpdate -Activity "SCCM Cache Clear" -Status "Removing cache items"
        $RemovalResults = Remove-CacheItems -ItemsToRemove $ClearingPlan.ItemsToRemove -TimeoutMinutes $MaxWaitTimeMinutes
        $RemediationResult.CacheOperations.Removal = $RemovalResults
        
        if ($RemovalResults.Issues.Count -gt 0) {
            $RemediationResult.Warnings += $RemovalResults.Issues
        }
        
        if ($RemovalResults.Failed -gt 0) {
            $RemediationResult.Warnings += "Failed to remove $($RemovalResults.Failed) cache items"
            foreach ($FailedItem in $RemovalResults.FailedItems) {
                Write-LogMessage "Failed item: $($FailedItem.ContentId) - $($FailedItem.Reason)" -Level Warning
            }
        }
        
        Write-LogMessage "Cache removal completed - Removed: $($RemovalResults.Successful), Failed: $($RemovalResults.Failed), Space recovered: $($RemovalResults.RemovedSizeMB) MB" -Level Success
    }
    else {
        Write-LogMessage "No cache items require removal" -Level Info
        $RemediationResult.CacheOperations.Removal = @{
            TotalAttempted = 0
            Successful = 0
            Failed = 0
            RemovedSizeMB = 0
            Duration = 0
            Issues = @()
        }
    }
    
    # Optimize cache configuration
    Write-ProgressUpdate -Activity "SCCM Cache Clear" -Status "Optimizing cache configuration"
    $OptimizationResults = Optimize-CacheConfiguration
    $RemediationResult.CacheOperations.Optimization = $OptimizationResults
    
    if ($OptimizationResults.Issues.Count -gt 0) {
        $RemediationResult.Warnings += $OptimizationResults.Issues
    }
    
    Write-LogMessage "Cache optimization completed" -Level Success
    
    # Validate cache health after clearing
    if ($ValidateAfterClear) {
        Write-ProgressUpdate -Activity "SCCM Cache Clear" -Status "Validating cache health"
        Start-Sleep -Seconds 5 # Allow time for system to update
        
        $ValidationResults = Test-CacheHealthAfterClear
        $RemediationResult.Details.PostClearValidation = $ValidationResults
        
        if ($ValidationResults.Issues.Count -gt 0) {
            $RemediationResult.Warnings += $ValidationResults.Issues
        }
        
        if (-not $ValidationResults.CacheAccessible -or -not $ValidationResults.ServiceHealthy) {
            Write-LogMessage "Cache health validation failed" -Level Error
            $RemediationResult.Errors += "Cache health validation failed after clearing"
            $RemediationResult.ExitCode = 2
        }
        else {
            Write-LogMessage "Cache health validation successful" -Level Success
        }
    }
    
    # Calculate performance metrics
    $OverallDuration = [math]::Round(((Get-Date) - $OverallStartTime).TotalMinutes, 2)
    $RemediationResult.Performance = @{
        TotalDurationMinutes = $OverallDuration
        ItemsProcessed = $ClearingPlan.ItemsToRemove.Count
        SpaceRecoveredMB = if ($RemediationResult.CacheOperations.Removal) { $RemediationResult.CacheOperations.Removal.RemovedSizeMB } else { 0 }
        SuccessRate = if ($ClearingPlan.ItemsToRemove.Count -gt 0) { [math]::Round(($RemediationResult.CacheOperations.Removal.Successful / $ClearingPlan.ItemsToRemove.Count) * 100, 1) } else { 100 }
    }
    
    # Determine overall success
    $TotalErrors = $RemediationResult.Errors.Count
    $TotalWarnings = $RemediationResult.Warnings.Count
    
    if ($TotalErrors -eq 0) {
