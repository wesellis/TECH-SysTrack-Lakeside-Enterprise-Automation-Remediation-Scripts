    $ClearingPlan = @{
        TotalItems = 0
        ItemsToRemove = @()
        ItemsToPreserve = @()
        EstimatedSavingsMB = 0
        RemovalStrategy = $Mode
        Issues = @()
    }
    
    try {
        # Get all cache items
        $AllCacheItems = Get-WmiObject -Namespace $CacheNamespace -Class "CacheInfoEx" -ErrorAction Stop
        if ($AllCacheItems) {
            $ClearingPlan.TotalItems = $AllCacheItems.Count
            $CurrentTime = Get-Date
            $AgeThreshold = $CurrentTime.AddDays(-$MaxAge)
            
            foreach ($Item in $AllCacheItems) {
                $ItemAnalysis = @{
                    ContentId = $Item.ContentId
                    Location = $Item.Location
                    SizeMB = [math]::Round($Item.ContentSize / 1MB, 2)
                    LastReferenced = $null
                    Age = 0
                    IsPersistent = $false
                    IsReferenced = $false
                    IsOrphaned = $false
                    IsStale = $false
                    ShouldRemove = $false
                    RemovalReason = ""
                }
                
                # Analyze item age and reference status
                try {
                    if ($Item.LastReferenceTime) {
                        $ItemAnalysis.LastReferenced = [Management.ManagementDateTimeConverter]::ToDateTime($Item.LastReferenceTime)
                        $ItemAnalysis.Age = ($CurrentTime - $ItemAnalysis.LastReferenced).Days
                        $ItemAnalysis.IsReferenced = $true
                        
                        if ($ItemAnalysis.LastReferenced -lt $AgeThreshold) {
                            $ItemAnalysis.IsStale = $true
                        }
                    }
                    else {
                        $ItemAnalysis.IsOrphaned = $true
                    }
                }
                catch {
                    $ItemAnalysis.IsOrphaned = $true
                }
                
                # Check if item is persistent
                try {
                    if ($Item.PersistInCache) {
                        $ItemAnalysis.IsPersistent = $true
                    }
                }
                catch {
                    # Property may not exist in all SCCM versions
                }
                
                # Check if content directory exists
                $ContentExists = $false
                if ($Item.Location -and (Test-Path $Item.Location)) {
                    $ContentFiles = Get-ChildItem -Path $Item.Location -ErrorAction SilentlyContinue
                    $ContentExists = ($ContentFiles.Count -gt 0)
                }
                
                if (-not $ContentExists -and $ItemAnalysis.IsReferenced) {
                    $ItemAnalysis.IsOrphaned = $true
                }
                
                # Apply clearing strategy
                switch ($Mode) {
                    'Selective' {
                        if ($ItemAnalysis.IsOrphaned) {
                            $ItemAnalysis.ShouldRemove = $true
                            $ItemAnalysis.RemovalReason = "Orphaned content"
                        }
                        elseif ($ItemAnalysis.IsStale -and (-not $ItemAnalysis.IsPersistent -or -not $PreservePersistent)) {
                            $ItemAnalysis.ShouldRemove = $true
                            $ItemAnalysis.RemovalReason = "Stale content (Age: $($ItemAnalysis.Age) days)"
                        }
                    }
                    'Aggressive' {
                        if ($ItemAnalysis.IsOrphaned -or $ItemAnalysis.IsStale) {
                            if (-not $ItemAnalysis.IsPersistent -or -not $PreservePersistent) {
                                $ItemAnalysis.ShouldRemove = $true
                                $ItemAnalysis.RemovalReason = if ($ItemAnalysis.IsOrphaned) { "Orphaned content" } else { "Stale content" }
                            }
                        }
                        elseif ($ItemAnalysis.Age -gt ($MaxAge / 2) -and (-not $ItemAnalysis.IsPersistent -or -not $PreservePersistent)) {
                            $ItemAnalysis.ShouldRemove = $true
                            $ItemAnalysis.RemovalReason = "Aging content (Age: $($ItemAnalysis.Age) days)"
                        }
                    }
                    'Complete' {
                        if (-not $ItemAnalysis.IsPersistent -or -not $PreservePersistent) {
                            $ItemAnalysis.ShouldRemove = $true
                            $ItemAnalysis.RemovalReason = "Complete cache clear"
                        }
                    }
                }
                
                # Add to appropriate collection
                if ($ItemAnalysis.ShouldRemove) {
                    $ClearingPlan.ItemsToRemove += $ItemAnalysis
                    $ClearingPlan.EstimatedSavingsMB += $ItemAnalysis.SizeMB
                }
                else {
                    $ClearingPlan.ItemsToPreserve += $ItemAnalysis
                }
            }
        }
