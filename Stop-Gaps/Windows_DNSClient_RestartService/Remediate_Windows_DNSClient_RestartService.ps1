        $stopwatch.Stop()
        
        if ($currentStatus -eq "Stopped") {
            Write-RemediationLog "DNS Client service stopped successfully in $([math]::Round($stopwatch.Elapsed.TotalSeconds, 2)) seconds"
            $script:RemediationSteps += "DNS Client service stopped successfully"
            return $true
