            Healthy = $false
            Issues = @()
            HealthScore = 0
            ProcessInfo = $null
        }
        
        # Check service status
        if ($Service.Status -eq 'Running') {
            $ServiceHealth.HealthScore += 40
        }
        else {
            $ServiceHealth.Issues += "Service is $($Service.Status) instead of Running"
        }
        
        # Check start type
        if ($Service.StartType -in @('Automatic', 'Manual')) {
            $ServiceHealth.HealthScore += 20
        }
        else {
            $ServiceHealth.Issues += "Service start type is $($Service.StartType)"
        }
        
        # Get process information if running
        if ($Service.Status -eq 'Running') {
            try {
