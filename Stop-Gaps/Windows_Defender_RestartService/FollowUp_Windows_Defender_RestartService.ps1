            $ProtectionStatus.EngineVersion = $ComputerStatus.AMEngineVersion
            
            # Calculate definition age
            if ($ComputerStatus.AntivirusSignatureLastUpdated) {
                $ProtectionStatus.DefinitionAge = ((Get-Date) - $ComputerStatus.AntivirusSignatureLastUpdated).TotalDays
