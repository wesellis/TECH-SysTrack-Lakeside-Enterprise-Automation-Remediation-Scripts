# Function to check for post-restart errors
function Test-PostRestartErrors {
    try {
        $ErrorCheck = @{
            NewErrors = @()
            CriticalErrors = @()
            ErrorCount = 0
            CriticalErrorCount = 0
        }
        
        $CheckStartTime = (Get-Date).AddHours(-1)  # Check last hour only
        
        # Check Windows Defender Operational log
        try {
            $DefenderErrors = Get-WinEvent -FilterHashtable @{
                LogName = 'Microsoft-Windows-Windows Defender/Operational'
                Level = 2  # Error level
                StartTime = $CheckStartTime
